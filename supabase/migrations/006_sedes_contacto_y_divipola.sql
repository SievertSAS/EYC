-- ─── Catálogo: departamentos ───

CREATE TABLE IF NOT EXISTS departamentos (
  id          BIGINT PRIMARY KEY, -- código DANE numérico
  codigo_dane TEXT NOT NULL UNIQUE,
  nombre      TEXT NOT NULL
);

ALTER TABLE departamentos ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Lectura autenticada" ON departamentos
  FOR SELECT TO authenticated USING (true);

CREATE POLICY "Escritura admin" ON departamentos
  FOR ALL TO authenticated
  USING (public.is_admin())
  WITH CHECK (public.is_admin());

-- ─── Catálogo: municipios ───

CREATE TABLE IF NOT EXISTS municipios (
  id              BIGINT PRIMARY KEY, -- código DANE numérico (incluye depto)
  departamento_id BIGINT NOT NULL REFERENCES departamentos(id),
  codigo_dane     TEXT NOT NULL UNIQUE,
  nombre          TEXT NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_municipios_departamento ON municipios(departamento_id);

ALTER TABLE municipios ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Lectura autenticada" ON municipios
  FOR SELECT TO authenticated USING (true);

CREATE POLICY "Escritura admin" ON municipios
  FOR ALL TO authenticated
  USING (public.is_admin())
  WITH CHECK (public.is_admin());

